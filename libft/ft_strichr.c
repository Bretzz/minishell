#include "libft.h"

int	ft_strichr(const char *s, int c)
{
	unsigned int	i;

	if (!s)
		return (0);
	c = (unsigned char)c;
	i = 0;
	while (s[i] != '\0' && s[i] != c)
		i++;
	if (s[i] == '\0' && c != '\0')
		return (0);
	return (i + 1);
}
